#include <iostream>
#include <fstream>
#include "save.h"
using namespace std;

// [문제] "1부터100" 파일에는 int가 100개 기록되어 있다.
// 읽어서 화면에 출력하라

//--------
int main()
//--------
{
	ifstream in{ "1부터 100까지" };
	
	if (!in) {
		cout << "해당 파일을 열 수 없습니다." << endl;
		return 0;
	}

	int num;
	
	while (in >> num) {
		cout << num << ' ';
	}
	
	cout << endl;

	save("소스.cpp");
}